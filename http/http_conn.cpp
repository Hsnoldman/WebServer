// 对文件描述符设置非阻塞
int setnonblocking(int fd)
{
    int old_option = fcntl(fd, F_GETFL);
    int new_option = old_option | O_NONBLOCK;
    fcntl(fd, F_SETFL, new_option);
    return old_option;
}

// 向epoll中添加需要监听的文件描述符
// 将内核事件表注册读事件，ET模式，选择开启EPOLLONESHOT
void addfd(int epollfd, int fd, bool one_shot, int TRIGMode)
{
    epoll_event event;
    event.data.fd = fd;

    /*
    events字段表示要监听的事件类型，可以是以下值之一：
        EPOLLIN：表示对应的文件描述符上有数据可读
        EPOLLOUT：表示对应的文件描述符上可以写入数据
        EPOLLET：表示将epoll设置为边缘触发模式
        EPOLLRDHUP：表示对端已经关闭连接，或者关闭了写操作端的写入
        EPOLLONESHOT：表示将事件设置为一次性事件
    */
    if (1 == TRIGMode)
        event.events = EPOLLIN | EPOLLET | EPOLLRDHUP;
    else
        event.events = EPOLLIN | EPOLLRDHUP;

    /*
    注册EPOLLONESHOT事件的文件描述符，操作系统最对触发其上的一个可读可写或异常事件，且只触发一次
    处理完毕之后，重置这个socket的上的EPOLLONESHOT事件（modfd()函数），等待下一个线程处理
    作用：保证一个socket连接在任意时刻都只被一个线程处理
    */
    if (one_shot)
        event.events |= EPOLLONESHOT;
    // epoll_ctl对文件描述符epollfd引用的epoll实例执行控制操作
    epoll_ctl(epollfd, EPOLL_CTL_ADD, fd, &event); // EPOLL_CTL_ADD：在文件描述符epollfd所引用的epoll实例上注册目标文件描述符fd，并将事件事件与内部文件链接到fd。
    setnonblocking(fd);
}

////向epoll中移除监听的文件描述符
// 从内核时间表删除描述符
void removefd(int epollfd, int fd)
{
    epoll_ctl(epollfd, EPOLL_CTL_DEL, fd, 0);
    close(fd);
}

// 重置socket上的EPOLLONESHOT事件
void modfd(int epollfd, int fd, int ev, int TRIGMode)
{
    epoll_event event;
    event.data.fd = fd;

    if (1 == TRIGMode)
        event.events = ev | EPOLLET | EPOLLONESHOT | EPOLLRDHUP;
    else
        event.events = ev | EPOLLONESHOT | EPOLLRDHUP;

    epoll_ctl(epollfd, EPOLL_CTL_MOD, fd, &event);
}

int http_conn::m_user_count = 0; // 用户总量，静态成员
int http_conn::m_epollfd = -1;

// 关闭连接，关闭一个连接，客户总量减一
void http_conn::close_conn(bool real_close)
{
    if (real_close && (m_sockfd != -1))
    {
        printf("close %d\n", m_sockfd);
        removefd(m_epollfd, m_sockfd);
        m_sockfd = -1;
        m_user_count--; // 用户端数量-1
    }
}

// 初始化连接,外部调用初始化套接字地址
void http_conn::init(int sockfd, const sockaddr_in &addr, char *root, int TRIGMode,
                     int close_log, string user, string passwd, string sqlname)
{
    m_sockfd = sockfd;
    m_address = addr;

    addfd(m_epollfd, sockfd, true, m_TRIGMode);
    m_user_count++; // 用户端数量+1

    // 当浏览器出现连接重置时，可能是网站根目录出错或http响应格式出错或者访问的文件中内容完全为空
    doc_root = root;
    m_TRIGMode = TRIGMode;
    m_close_log = close_log;

    strcpy(sql_user, user.c_str());
    strcpy(sql_passwd, passwd.c_str());
    strcpy(sql_name, sqlname.c_str());

    init();
}

